package basics;

public class Regex_RegularExpressions {

	public static void main(String[] args) {

		// Regex means regular expressions.
		// you can use the back slash (\) to ignore the next character.
		// Or use (\n) to begin a new line Or (\t) to put a tab or indent.
		
		String temp = "\t this \"is\" a \n\n\n test";
		
		System.out.println(temp);
		
	}

}
